  private void jButton_RemoveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton_RemoveActionPerformed

    /* Determine the selected rows and remove entries from the interactionList */
    int selectedRows[] = jTable_CouplingOpList.getSelectedRows();

    if (selectedRows.length > 0) {
      int k;
      int N = tableData_InteractionList.interactionList.length;
      int mask[] = new int[N];
      int I;
      int newN;
      SELM_Interaction[] interactionList_new;

      for (k = 0; k < N; k++) {
        mask[k] = 1; /* indicates to keep this entry */
      }

      newN = N;
      for (k = 0; k < selectedRows.length; k++) {
        I = selectedRows[k];
        mask[I] = 0; /* indicates this is row to delete */
        newN--;
      }

      interactionList_new = new SELM_Interaction[newN];
      I = 0;
      for (k = 0; k < N; k++) {
        if (mask[k] == 1) {
          interactionList_new[I] = tableData_InteractionList.interactionList[k];
          I++;
        }
      }

      tableData_InteractionList.interactionList = interactionList_new;

      setDataValue(tableData_InteractionList, "Name", "Type");

      /* set the selected row */
      if (selectedRows[0] < newN) {
        jTable_CouplingOpList.setRowSelectionInterval(selectedRows[0], selectedRows[0]);
      } else if (newN > 0) {
        jTable_CouplingOpList.setRowSelectionInterval(newN - 1, newN - 1);
      }

    }
                    
  }//GEN-LAST:event_jButton_RemoveActionPerformed

  private void jComboBox_Add_InteractionActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jComboBox_Add_InteractionActionPerformed

    String selectedType = (String) jComboBox_Add_Interaction.getSelectedItem();

    SELM_Interaction   newInteraction = null;
    SELM_Interaction[] interactionList_new;
   
    if (selectedType.equals("PAIRS_HARMONIC")) {

      SELM_Interaction_PAIRS_HARMONIC newInteraction_PAIRS_HARMONIC
        = new SELM_Interaction_PAIRS_HARMONIC();

      newInteraction_PAIRS_HARMONIC.InteractionName    = "Name Here";
      
      newInteraction = newInteraction_PAIRS_HARMONIC;
    }

    if (selectedType.equals("PAIRS_TABLE")) {

      SELM_Interaction_PAIRS_TABLE newInteraction_PAIRS_TABLE
        = new SELM_Interaction_PAIRS_TABLE();

      newInteraction_PAIRS_TABLE.InteractionName    = "Name Here";

      newInteraction = newInteraction_PAIRS_TABLE;
    }

    if (newInteraction != null) {
      /* add the interaction to the list */
      int N = tableData_InteractionList.interactionList.length;
      interactionList_new = new SELM_Interaction[N + 1];
      System.arraycopy(tableData_InteractionList.interactionList, 0,
              interactionList_new, 0, N);

      interactionList_new[N] = newInteraction;

      tableData_InteractionList.interactionList = interactionList_new;

      setDataValue(tableData_InteractionList,"Name","Type");
    }

    /* reset the selection to zero */
    jComboBox_Add_Interaction.setSelectedIndex(0);

  }//GEN-LAST:event_jComboBox_Add_InteractionActionPerformed

