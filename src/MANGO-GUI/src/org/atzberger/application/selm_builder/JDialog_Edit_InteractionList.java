  public void setDataValue(Object dataValue, String dataName, String dataType) {

    SELM_Interaction interaction;
    String name;
    int k, N;

    TableModel_InteractionList tableModel_InteractionList;

    super.setDataValue(dataValue, dataName, dataType);

    tableModel_InteractionList = (TableModel_InteractionList) jTable_CouplingOpList.getModel();

    tableData_InteractionList = (TableData_InteractionList) dataValue;

    /* setup the table values */
    N = tableData_InteractionList.interactionList.length;
    for (k = 0; k < N; k++) {
      interaction = tableData_InteractionList.interactionList[k];
      
      tableModel_InteractionList.setValueAt(interaction.InteractionName, k, 0, tableModel_InteractionList.EDITABLE);
      tableModel_InteractionList.setValueAt(interaction.InteractionTypeStr, k, 1, tableModel_InteractionList.NOT_EDITABLE);

    }

    tableModel_InteractionList.maxRowUsed = N - 1;

    tableModel_InteractionList.fireTableDataChanged();

    
  }

  public void tableChanged(TableModelEvent e) {

    int row    = e.getFirstRow();
    int column = e.getColumn();

    SELM_Interaction interaction;

    TableModel model = (TableModel) e.getSource();

    Object data       = model.getValueAt(row, column); /* not column -1 since not selectable */
       
    if ( (data != null) && (e.getType() == e.UPDATE) && (column == 0) ) {            
      /* update all the interaction data */      
      interaction                                    = tableData_InteractionList.interactionList[row];
      interaction.InteractionName                    = (String) model.getValueAt(row, 0);
      //tableData_InteractionList.interactionList[row] = interaction;
    }

  }




