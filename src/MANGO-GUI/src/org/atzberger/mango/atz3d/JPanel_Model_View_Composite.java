    private void jButton_Reset_Camera_ViewActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton_Reset_Camera_ViewActionPerformed

      JPanel_Model_View_RenderPanel renderControlPts = (JPanel_Model_View_RenderPanel)jPanel_Model_View_RenderPanel;

      if ("Reset_View_pressed".equals(evt.getActionCommand())) {
        renderControlPts.resetCameraView();

        /* also select the rotation mode as default */
        renderControlPts.setInterfaceMode(renderControlPts.interfaceMode_Navigation); /* replace with constants */
        buttonGroup_Model_View_Controls.setSelected(jToggleButton_Rotation_Mode.getModel(), true);

        renderControlPts.repaint();

        //jButton_ControlPts_Reset_View.setSelected(false); /* avoid having this selected */
      }
}//GEN-LAST:event_jButton_Reset_Camera_ViewActionPerformed

