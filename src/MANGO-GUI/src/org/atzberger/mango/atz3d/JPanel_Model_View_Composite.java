    private void jPanel_Bottom_Containing_Render_PanelComponentResized(java.awt.event.ComponentEvent evt) {//GEN-FIRST:event_jPanel_Bottom_Containing_Render_PanelComponentResized
      
      Dimension sizeContainer    = jPanel_Bottom_Containing_Render_Panel.getSize();
      Dimension sizeContainerAdj = new Dimension();
      
      sizeContainerAdj.width  = (int) java.lang.Math.round(1.0*sizeContainer.width);
      sizeContainerAdj.height = (int) java.lang.Math.round(1.0*sizeContainer.height);

      jPanel_Model_View_RenderPanel.setSize(sizeContainerAdj);

      Dimension sizeRenderPort = jPanel_Model_View_RenderPanel.getSize();

      int xx = (sizeContainer.width/2) - (sizeRenderPort.width/2);
      int yy = (sizeContainer.height/2) - (sizeRenderPort.height/2);
      jPanel_Model_View_RenderPanel.setLocation(xx, yy);
      
      repaint();
      
    }//GEN-LAST:event_jPanel_Bottom_Containing_Render_PanelComponentResized

